#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	ch;

	if (n <= 9 && n >= 0)
		ch = n + '0';
	if (n >= -9 && n <= -1)
	{
		write(fd, "-", 1);
		ch = -n + '0';
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ch = n % 10 + '0';
	}
	if (n < -9)
	{
		write(fd, "-", 1);
		ft_putnbr_fd((n / 10) * (-1), fd);
		ch = (n % 10) *(-1) + '0';
	}
	write(fd, &ch, 1);
}
